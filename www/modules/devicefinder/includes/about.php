<div style="font-family: courier, monospace">
Command line usage: ./ik_wifi_device_finder.py -i INTERFACE [-m MAC | -e SSID] {options}
<br>
<br>Options:
<br>-i INTERFACE      &nbsp;&nbsp;        WLAN Interface (monitor mode required)
<br>-m MAC            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;        MAC address to be found
<br>-e SSID           &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;              SSID name to be found
<br>-t TIME           &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;              Target gone time (default 10 seconds)
<br>-l LOG            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;        Log path
<br>-p POWER_BAR      &nbsp;&nbsp;                                            Power Bar
<br>-h                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;        This help
